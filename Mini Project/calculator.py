def get_number(prompt: str) -> float:
    """Prompt user and type-cast input to float."""
    while True:
        try:
            return float(input(prompt))
        except ValueError:
            print("  âš   Invalid input. Please enter a number.\n")

def get_numbers_list() -> list[float]:
    """Collect a list of numbers from the user."""
    print("\n  Enter numbers one by one. Type 'done' when finished.")
    nums: list[float] = []
    while True:
        raw = input(f"  Number [{len(nums)+1}]: ").strip()
        if raw.lower() == "done":
            if len(nums) < 1:
                print("  âš   Enter at least one number.")
                continue
            return nums
        try:
            nums.append(float(raw))
        except ValueError:
            print("  âš   Invalid input. Try again.")

