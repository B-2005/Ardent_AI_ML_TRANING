def statistics_menu():
    header("STATISTICS  ( Mean Â· Median Â· Mode Â· Avg )")
    print("  1) Mean (Average)")
    print("  2) Median")
    print("  3) Mode")
    print("  4) All of the above + Sum, Min, Max")
    choice = input("  Choose [1-4]: ").strip()

    if choice not in ("1", "2", "3", "4"):
        print("  âš   Invalid choice.")
        return

    nums = get_numbers_list()
    print()

    if choice == "1" or choice == "4":
        mean = statistics.mean(nums)
        print(f"  Mean (Average) : {mean}")

    if choice == "2" or choice == "4":
        median = statistics.median(nums)
        print(f"  Median         : {median}")

    if choice == "3" or choice == "4":
        freq = Counter(nums)
        max_freq = max(freq.values())
        modes = [k for k, v in freq.items() if v == max_freq]
        if len(modes) == len(set(nums)):
            print(f"  Mode           : No mode (all values appear equally)")
        else:
            print(f"  Mode           : {', '.join(str(m) for m in modes)}"
                  f"  (appears {max_freq}x)")

    if choice == "4":
        print(f"  Sum            : {sum(nums)}")
        print(f"  Min            : {min(nums)}")
        print(f"  Max            : {max(nums)}")
        print(f"  Count          : {len(nums)}")


