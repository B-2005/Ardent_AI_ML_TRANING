def percentage_menu():
    header("PERCENTAGE CALCULATOR")
    print("  1) X% of a number          e.g. 20% of 150  â†’ 30")
    print("  2) What % is A of B?       e.g. 30 of 150   â†’ 20%")
    print("  3) % Increase / Decrease   e.g. 100 â†’ 120   â†’ +20%")
    choice = input("  Choose [1-3]: ").strip()

    if choice == "1":
        pct = get_number("  Enter percentage (e.g. 20 for 20%): ")
        num = get_number("  Enter the number                   : ")
        result = (pct / 100) * num
        print(f"\n  Result : {pct}% of {num} = {result}")

    elif choice == "2":
        a = get_number("  Enter value A  : ")
        b = get_number("  Enter total B  : ")
        if b == 0:
            print("  âš   B cannot be zero.")
            return
        result = (a / b) * 100
        print(f"\n  Result : {a} is {result:.4f}% of {b}")

    elif choice == "3":
        old = get_number("  Enter original value : ")
        new = get_number("  Enter new value      : ")
        if old == 0:
            print("  âš   Original value cannot be zero.")
            return
        change = ((new - old) / abs(old)) * 100
        direction = "increase" if change >= 0 else "decrease"
        print(f"\n  Result : {old} â†’ {new} is a {abs(change):.4f}% {direction}")

    else:
        print("  âš   Invalid choice.")


