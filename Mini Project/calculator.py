def basic_arithmetic():
    header("BASIC ARITHMETIC  ( +  âˆ’  Ã—  Ã· )")
    a = get_number("  Enter first number  : ")
    print("  Operations: 1) Add  2) Subtract  3) Multiply  4) Divide")
    op = input("  Choose operation [1-4]: ").strip()

    ops = {"1": "+", "2": "-", "3": "*", "4": "/"}
    if op not in ops:
        print("  âš   Invalid choice.")
        return

    b = get_number("  Enter second number : ")

    if op == "4" and b == 0:
        print("\n  âš   Cannot divide by zero.")
        return

    symbol = ops[op]
    result: float = eval(f"{a} {symbol} {b}")   # simple & safe â€” both are floats
    print(f"\n  Result : {a} {symbol} {b} = {result}")


